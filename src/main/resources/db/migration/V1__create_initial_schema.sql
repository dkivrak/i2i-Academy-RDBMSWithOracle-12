CREATE TABLE authors (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(255) NOT NULL UNIQUE
);

CREATE TABLE publishers (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(255) NOT NULL UNIQUE
);

CREATE TABLE books (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR2(255) NOT NULL,
    author_id NUMBER NOT NULL,
    publisher_id NUMBER NOT NULL,
    CONSTRAINT fk_books_author FOREIGN KEY (author_id) REFERENCES authors(id),
    CONSTRAINT fk_books_publisher FOREIGN KEY (publisher_id) REFERENCES publishers(id)
);

CREATE TABLE audit_logs (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    book_id NUMBER,
    action_type VARCHAR2(50),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    db_user VARCHAR2(100)
);

CREATE OR REPLACE TRIGGER trg_books_insert_audit
AFTER INSERT ON books
FOR EACH ROW
BEGIN
    INSERT INTO audit_logs (book_id, action_type, created_at, db_user)
    VALUES (:NEW.id, 'INSERT', CURRENT_TIMESTAMP, USER);
END;
